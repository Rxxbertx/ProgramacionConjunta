package excepcion;

public class Operaciones {
	
	int numero1;
	int numero2;
	
	public Operaciones(int numero1, int numero2) {
		this.numero1=numero1;
		this.numero2=numero2;
		
	}

	public int dividir() {

		
		return numero1/numero2;
		
	}

	public  int suma() {

		
		
		return numero1+numero2;
		
	}
	

}
